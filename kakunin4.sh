#!/bin/bash

# 最大公約数を計算するスクリプトtest4.shのテスト
function test_gcd {
  result=$(./test4.sh $1 $2)
  if [ "$result" -eq "$3" ]; then
    echo "テスト成功: $1 と $2 の最大公約数は $3"
  else
    echo "テスト失敗: $1 と $2 の最大公約数は $3 のはずが $result が返されました"
    exit 1
  fi
}

# 引数が少ないケースのテスト
echo "引数が少ないケースのテスト"
./test4.sh 2 || echo "エラー: 引数が少ない場合にエラーメッセージが出なかった"

# 引数が多いケースのテスト
echo "引数が多いケースのテスト"
./test4.sh 2 3 4 || echo "エラー: 引数が多い場合にエラーメッセージが出なかった"

# さまざまなテストケースでテスト
echo "通常のケースのテスト"
test_gcd 2 4 2
test_gcd 15 10 5
test_gcd 121 11 11

# 負の数のテスト
echo "負の数のケースのテスト"
./test4.sh -2 4 || echo "エラー: 負の数が入力された場合にエラーメッセージが出なかった"

# 小数のテスト
echo "小数のケースのテスト"
./test4.sh 2.5 4 || echo "エラー: 小数が入力された場合にエラーメッセージが出なかった"

# 全てのテストがパスした
echo "全てのテストが成功しました！"

